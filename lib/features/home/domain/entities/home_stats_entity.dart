class HomeStatsEntity {
  final String salesValue;
  final String salesPercentage;

  final String profitsValue;
  final String profitsPercentage;

  final String ordersValue;
  final String ordersPercentage;

  final String reportsValue;
  final String reportsPercentage;

  HomeStatsEntity({
    required this.salesValue,
    required this.salesPercentage,
    required this.profitsValue,
    required this.profitsPercentage,
    required this.ordersValue,
    required this.ordersPercentage,
    required this.reportsValue,
    required this.reportsPercentage,
  });
}